export function getTrackById(id: string): LearningTrack | undefined {
  return learningTracks.find((t) => t.id === id);
}

export function getChapterById(id: string): LearningChapter | undefined {
  for (const track of learningTracks) {
    const chapter = track.chapters.find((c) => c.id === id);
    if (chapter) return chapter;
  }
  return undefined;
}
