export function getDaoismChapterById(id: string): DaoismChapter | undefined {
  for (const cat of daoismCategories) {
    const chapter = cat.chapters.find((c) => c.id === id);
    if (chapter) return chapter;
  }
  return undefined;
}

export function getDaoismCategoryById(id: string): DaoismCategory | undefined {
  return daoismCategories.find((c) => c.id === id);
}
